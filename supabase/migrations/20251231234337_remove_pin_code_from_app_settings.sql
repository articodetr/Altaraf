/*
  # إزالة حقل pin_code من جدول app_settings

  ## التغييرات

  1. **إزالة حقل pin_code**
    - حذف عمود `pin_code` من جدول `app_settings`
    - هذا الحقل لم يعد مستخدماً لأننا انتقلنا إلى نظام المصادقة الجديد
    - نظام المصادقة الآن يعتمد فقط على جدول `app_security` مع PIN معقد (8-16 حرف)

  ## الأسباب

  - توحيد نظام المصادقة: بدلاً من نظامين (PIN بسيط في app_settings + PIN معقد في app_security)
  - الآن نستخدم فقط نظام app_security للمصادقة
  - تحسين الأمان: كلمة مرور معقدة 8-16 حرف مع SHA256 hashing بدلاً من رمز بسيط (1234)

  ## التأثير

  - لن يؤثر على البيانات الحالية
  - جدول app_settings سيحتفظ بجميع الإعدادات الأخرى (اسم المحل، الهاتف، الشعار، إلخ)
  - المستخدمون سيحتاجون فقط لإدخال كلمة المرور الجديدة (8-16 حرف) عند تسجيل الدخول
*/

-- Drop the pin_code column from app_settings table
ALTER TABLE app_settings
DROP COLUMN IF EXISTS pin_code;
